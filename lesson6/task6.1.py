d2 = n % 100 // 10
d3 = n // 100
s = d1 + d2 + d3
m = d1 * d2 * d3
print("Сумма цифр числа:", s)
print("Произведение цифр числа:", m)
print(f' выделено памяти {sys.getsizeof(s)}')   # выделено памяти 28
print(f' выделено памяти {sys.getsizeof(m)}')   # выделено памяти 28
print(f' создано ссылок на объект {sys.getrefcount(s)}')  # создано ссылок на объект 7
