a = int(input('Введите число: '))
a_rev = 0
while a > 0:
    a_rev = a_rev * 10 + a % 10
    a = a // 10
print('Ваше число в обратном порядке: ', a_rev)
print(f' выделено памяти {sys.getsizeof(a_rev)}')  # выделено памяти 28
print(f' создано ссылок на объект {sys.getrefcount(a_rev)}')  # создано ссылок на объект 2

